-- Q8- top 5 products with highest discount usages %

SELECT 
	item_purchased,
    COUNT(item_purchased) as total_no_item_sold,
    COUNT(CASE WHEN discount_applied = 'Yes' THEN 1 END) AS number_of_time_sold_discount_applied,
    COUNT(CASE WHEN discount_applied = 'Yes' THEN 1 END)  *100.0 / COUNT(*) AS percent_ofdiscount
FROM customer_shopping_details
GROUP BY  item_purchased
ORDER BY percent_ofdiscount desc
LIMIT 5;

-- Q9- Segment customer into new, returning and loyal based on thier purchase and show the count of each segment

SELECT 
	CASE
		WHEN Previous_Purchases='1' THEN 'NEW CUSTOMER'
		WHEN Previous_Purchases BETWEEN 5 AND 15 THEN 'Returning customer'
	ELSE 'Loyal Customers'
END  AS customer_segment,
COUNT(*) AS customers_count
FROM customer_shopping_details
GROUP BY customer_segment;

-- Q10- What are the top 3 most purchased item within each category 


WITH CTE AS
(
select
	category,
    item_purchased,
    COUNT(item_purchased) as most_purchased,
    RANK() OVER(PARTITION BY category ORDER BY COUNT(item_purchased) desc ) as rank_item
FROM customer_shopping_details
GROUP BY category, item_purchased    
)
SELECT  *FROM CTE 
WHERE rank_item <= 3
;


-- Q11= Are customer who are repeat buyer also likely to suscribe ?

