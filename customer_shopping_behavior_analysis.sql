-- Q2- Are discount actually increasing purchase value

SELECT 
discount_applied,
ROUND(SUM(purchase_amount), 2) AS total_revenue ,
ROUND(AVG(purchase_amount), 2) AS avg_purchase 
FROM 
customer_shopping_details
GROUP BY discount_applied;


-- Q3 - What is the total revenue generated by male and female customers

SELECT 	gender, ROUND(SUM(purchase_amount), 2) AS total_revenue  FROM 
customer_shopping_details
GROUP BY gender;


-- Q4- Which customer using a discount but still spent more then the avg purchased amount


SELECT 
customer_ID,
purchase_amount,
discount_applied
FROM customer_shopping_details 
WHERE  discount_applied='Yes' AND purchase_amount > (SELECT AVG(purchase_amount) FROM customer_shopping_details);



-- Q5- Which are the top/bottom 5 product  which has highest avg  review rating 

SELECT 
	item_purchased,
	ROUND(AVG(review_rating), 2) AS avg_rating
FROM customer_shopping_details
group by item_purchased
ORDER BY avg_rating DESC
LIMIT 5;


SELECT 
	item_purchased,
	ROUND(AVG(review_rating), 2) AS avg_bottom_rating
FROM customer_shopping_details
group by item_purchased
ORDER BY avg_bottom_rating ASC
LIMIT 5;



-- Q6-Average purchase:standard vs Express shipping

SELECT 
	shipping_type,
    Count(DISTINCT customer_ID ) as order_placed,
    ROUND(AVG(purchase_amount), 2) as avg_purchase
FROM customer_shopping_details
WHERE shipping_type IN ('Express','Standard')
GROUP BY shipping_type;


-- Q7- Do suscrie customers spend more AS compare avg speed and total revenue beteween sub or non sub

SELECT 
	subscription_status,
    COUNT(customer_ID) as users,
    ROUND(SUM(Frequency_of_Purchases),2) as avg_speed,
